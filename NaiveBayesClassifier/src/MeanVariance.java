public class MeanVariance {
    double mean;
    double variance;

    MeanVariance(double mean, double variance) {
        this.mean = mean;
        this.variance = variance;
    }

    double getMean() {
        return mean;
    }

    double getVariance() {
        return variance;
    }
}
